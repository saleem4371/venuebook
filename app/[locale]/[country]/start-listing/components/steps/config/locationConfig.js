/** Convenience: get config by code, fallback to IN */
export function getLocationConfig(code) {
  return LOCATION_CONFIG[code?.toUpperCase()] || LOCATION_CONFIG.IN;
}

/** Convenience: get country display name from code */
export function getCountryName(code) {
  return COUNTRY_LIST.find(c => c.code === code)?.name || code || "";
}
